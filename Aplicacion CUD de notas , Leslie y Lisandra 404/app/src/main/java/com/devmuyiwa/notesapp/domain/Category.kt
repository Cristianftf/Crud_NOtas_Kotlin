package com.devmuyiwa.notesapp.domain

// Enumeración de las categorías posibles para las notas
enum class Category {
    // Categoría para notas relacionadas con el trabajo
    WORK, 
    // Categoría para notas relacionadas con el hogar
    HOME,
    // Categoría para notas relacionadas con estudios
    STUDY,
    // Categoría para notas personales
    PERSONAL,
    // Categoría por defecto para notas sin categorizar
    UNCATEGORIZED  
}